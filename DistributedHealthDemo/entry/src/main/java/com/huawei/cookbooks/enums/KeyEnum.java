package com.huawei.cookbooks.enums;

/**
 * 健康数据枚举类
 *
 * @since 2021-05-21
 */
public enum KeyEnum {
    LATEST_STEP("latestStep", "最新步数"),
    LATEST_RATE("latestRate", "最新心率"),
    RATE("rate", "心率数据"),
    STEP("step", "步数数据");

    private String value;

    private String desc;

    KeyEnum(String value, String desc) {
        this.value = value;
        this.desc = desc;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
